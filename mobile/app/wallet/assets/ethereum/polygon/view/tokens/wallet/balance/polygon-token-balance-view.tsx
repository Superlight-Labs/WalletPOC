import { ERC20 } from "@maticnetwork/maticjs/dist/ts/pos/erc20";
import { ERC20Token } from "ethereum/config/tokens";
import { ethers } from "ethers";
import React, { useEffect, useState } from "react";
import { ActivityIndicator, Image, StyleSheet, Text, TouchableOpacity, View } from "react-native";

type TokenBalanceProps = {
  address: string;
  childErc20: ERC20;
  token: ERC20Token;
};

export const TokenBalanceView = ({ address, token, childErc20 }: TokenBalanceProps) => {
  const [loading, setLoading] = useState<boolean>(false);
  const [tokenBalance, setTokenBalance] = useState<string>();

  useEffect(() => {
    updateBalance();
  }, []);

  const updateBalance = () => {
    const loadBalance = async () => {
      setLoading(true);

      const balance = await childErc20.getBalance(address);

      setTokenBalance(balance);
      setLoading(false);
    };
    loadBalance();
  };

  return (
    <View style={styles.balanceContainer}>
      <View style={{ flexDirection: "row" }}>
        <Text style={styles.balanceText}>
          {tokenBalance ? ethers.utils.formatUnits(tokenBalance, token.decimals) : "0"} {token.symbol}
        </Text>
        {loading && <ActivityIndicator />}
      </View>
      <TouchableOpacity onPress={updateBalance}>
        <Image
          style={styles.reloadIcon}
          source={{
            uri: "https://cdn.iconscout.com/icon/free/png-256/reload-retry-again-update-restart-refresh-sync-13-3149.png",
          }}
        />
      </TouchableOpacity>
    </View>
  );
};

const styles = StyleSheet.create({
  balanceContainer: {
    flexDirection: "row",
    justifyContent: "space-between",
    alignItems: "center",
  },
  balanceText: {
    fontSize: 24,
    fontWeight: "normal",
    marginRight: 8,
  },
  reloadIcon: {
    width: 20,
    height: 20,
  },
});
